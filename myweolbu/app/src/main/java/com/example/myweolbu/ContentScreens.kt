@Composable
fun webViewPage(url: String) {
    AndroidView(factory = {
        val apply = WebView(it).apply {
            layoutParams = ViewGroup.LayoutParams(
                ViewGroup.LayoutParams.MATCH_PARENT,
                ViewGroup.LayoutParams.MATCH_PARENT
            )
            webViewClient = WebViewClient()
            loadUrl(url)
        }
        apply.settings.javaScriptEnabled = true;
        apply
        }, update = {
        it.loadUrl(url)
    })
}